import AOS from 'aos';

export default {
  data() {
    return {
      products: [],
      loading: false,
      error: '',
    };
  },
  computed: {
    categorizedProducts() {
      const categorized = {};
      this.products.forEach((product) => {
        const category = product.Categories || 'Uncategorized';
        if (!categorized[category]) {
          categorized[category] = [];
        }
        categorized[category].push(product);
      });
      return categorized;
    },
  },
